"convert list to tuple"

sample_list = ['a', 2, 3.5, 4, True]
print(tuple(sample_list))

"convert int to complex"

x = 10
print(complex(x))

"convert complex to bool"

c = 10+2j
print(bool(c))

"convert string to bool"

str1 = "Hello World"
print(bool(str1))
str2 = ""
print(bool(str2))

"convert list of tuple to dict"

list1 = [('k1', 100), ('k2', 200), ('k3', 300), ('k4', 400)]
print(dict(list1))

"convert character to integer"

char1 = 'a'
print(ord(char1))

"convert integer to octal string"

y = 11
print(oct(y))

